public class Car {
    private int carGasAmount;
    private int capacity;

    public Car() {
        this.carGasAmount = 0;
        this.capacity = 100;
    }

    public int getCarGasAmount() {
        return carGasAmount;
    }

    public void setCarGasAmount(int carGasAmount) {
        this.carGasAmount = carGasAmount;
    }

    public int getCapacity() {
        return capacity;
    }
}
